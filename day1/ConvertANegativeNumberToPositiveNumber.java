public class ConvertANegativeNumberToPositiveNumber {

	public static void main(String[] args) {

		int number = -40;
		
		if(number < 0)
			
		{
			System.out.println("Any number that is lesser than zero is a Negative number ");
		}
		else  
		{
			System.out.println("Any number that is greater than zero is a Positive number ");
		}

	}

}
